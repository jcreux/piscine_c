int		ft_strcmp(char *a1, char *a2)
{
	int i;
	int diff;

	i = 0;
	diff = 0;
	while (a1[i] == a2[i] && a1[i] != '\0' && a2[i] != '\0')
	{
		i++;
	}
	diff = a1[i] - a2[i];
	return (diff);
}
